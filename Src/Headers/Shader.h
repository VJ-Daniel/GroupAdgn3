#ifndef SHADER_H
#define SHADER_H
#include <glm.hpp>
#include <string>

class Shader
{
private:

    //--------------------------------------------------
    // OpenGL Program
    //--------------------------------------------------

    unsigned int programID;

public:

    //--------------------------------------------------
    // Constructor
    //--------------------------------------------------

    Shader(
        const char* vertexPath,
        const char* fragmentPath);

    //--------------------------------------------------
    // Uniform 
    //--------------------------------------------------

    void SetUniform(
        const char* name,
        int value);

    //--------------------------------------------------
    // Matrices
    //--------------------------------------------------
	void SetMat4(
		const std::string& name,
		const glm::mat4& matrix);

    //--------------------------------------------------
    // Usage
    //--------------------------------------------------

    void Use();

    //--------------------------------------------------
    // Accessor
    //--------------------------------------------------

    unsigned int GetID() const;

    //--------------------------------------------------
    // Destructor
    //--------------------------------------------------

    ~Shader();
};

#endif